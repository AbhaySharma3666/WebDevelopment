/*
Assign method
  In JavaScript, the Object.assign() method is used to copy the values of all enumerable own properties from one or more source objects to a target object. It returns the target object after copying the properties.

  
Object.assign(target, source1, source2, ...sources);

target: The target object to which the properties will be copied. This object will be modified and returned.

sources: One or more source objects from which the properties will be copied to the target object.


*/

const person = {
  name: 'Mayank',
  age: 21
};

const clonedPerson = Object.assign({}, person);

console.log(clonedPerson); 

// Output : { name: 'Mayank', age: 21 }
