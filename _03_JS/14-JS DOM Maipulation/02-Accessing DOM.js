/*
    Accessing DOM Elements
        Accessing DOM (Document Object Model) elements is a fundamental aspect of web development, enabling us to interact with and manipulate the structure of web pages dynamically.
        access by id, class, tag name, query selector, and more.
*/


// 1. Accessing by ID
    // Syntax: document.getElementById('id')

    document.getElementById("codehelp");

// 2. Accessing by Class Name
    // Syntax: document.getElementsByClassName('className')

    document.getElementsByClassName("code");

// 3. Accessing by Tag Name
    // Syntax: document.getElementsByTagName('tagName')

    document.getElementsByTagName("h1");
    
// 4. Accessing by Query Selector
    // Syntax: document.querySelector('selector')

    document.querySelector("#codehelp"); // Accessing by ID
    document.querySelector(".code"); // Accessing by Class
    document.querySelector("h1"); // Accessing by Tag Name
    document.querySelector("div > h1"); // Accessing by CSS Selector    

// 5. Accessing by Query Selector All
    // Syntax: document.querySelectorAll('selector')

    document.querySelectorAll(".code"); // Accessing by Class
